function sum(a, b){
  return a + b;
}

function sub(a, b){
  return a - b;
}

function divide(a, b){
  return a / b;
}

function multiple(a, b){
  return a * b;
}

function higherNumber(a, b){
  if (a < b) {
    return b;
  }else{
    return a;
  }
}

